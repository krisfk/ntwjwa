<script type="text/javascript">
$(function() {



    Pace.on("done", function() {
        $('.main-container').fadeIn(0)
        checkvisible();

        var hash = window.location.hash.substring(1);

        if (hash) {
            $('a[rel="' + hash + '"]').addClass('active');
            $('a[rel="' + hash + '"]').next('.service-content').slideDown(200)

            $('body,html').scrollTop($('a[rel="' + hash + '"]').next('.service-content').offset().top)


        }


    });

    $(window).on(' scroll', function() {
        checkvisible();

    });


    $('.h2-a').click(function() {

        window.location.hash = $(this).attr('rel');
        $('body,html').scrollTop($(this).offset().top);

        $('.h2-a').removeClass('active');
        $(this).toggleClass('active');
        if ($(this).hasClass('active')) {
            $(this).next('.service-content').slideDown(200)
        } else {
            $('.service-content').fadeOut(0)
        }
        for (i = 0; i < $('.h2-a').length; i++) {
            if (!$('.h2-a').eq(i).hasClass('active')) {
                $('.h2-a').eq(i).next('.service-content').fadeOut(0)
            }
        }
    })
})

function checkvisible() {
    for (i = 0; i < $('.fadein-ele').length; i++) {
        if (
            $('.fadein-ele').eq(i).isInViewport() &&
            !$('.fadein-ele').eq(i).hasClass('animate__animated')
        ) {
            $('.fadein-ele').eq(i).addClass('animate__animated');
            $('.fadein-ele').eq(i).addClass('animate__fadeIn');
            $('.fadein-ele').eq(i).addClass('delay-2');
        }
    }
    for (i = 0; i < $('.fadein-ele2').length; i++) {
        if (
            $('.fadein-ele2').eq(i).isInViewport() &&
            !$('.fadein-ele2').eq(i).hasClass('animate__animated')
        ) {
            $('.fadein-ele2').eq(i).addClass('animate__animated');
            $('.fadein-ele2').eq(i).addClass('animate__fadeIn');
            $('.fadein-ele2').eq(i).addClass('delay-4');
        }
    }
    for (i = 0; i < $('.fadeinup-ele').length; i++) {
        if (
            $('.fadeinup-ele').eq(i).isInViewport() &&
            !$('.fadeinup-ele').eq(i).hasClass('animate__animated')
        ) {
            $('.fadeinup-ele').eq(i).addClass('animate__animated');
            $('.fadeinup-ele').eq(i).addClass('animate__fadeInUp');
            $('.fadeinup-ele').eq(i).addClass('delay-3');
        }
    }
}
</script>



